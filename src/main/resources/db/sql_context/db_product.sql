CREATE TABLE ProductMeta (
    productID INT UNSIGNED NOT NULL,
    additionalType VARCHAR(255),
    alternateName VARCHAR(255),
    disambiguatingDescription VARCHAR(255),
    identifier VARCHAR(255),
    image VARCHAR(255),
    mainEntityOfPage VARCHAR(255),
    mobileUrl VARCHAR(255),
    potentialAction JSON,
    sameAs VARCHAR(255),
    subjectOf VARCHAR(255),
    about VARCHAR(255),
    creator VARCHAR(255),
    contributor VARCHAR(255),
    publisher VARCHAR(255),
    datePublished DATE,
    dateModified DATE,
    version VARCHAR(255),
    url VARCHAR(255),
    FOREIGN KEY (productID) REFERENCES Product(id)
);

CREATE TABLE ProductReviews (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    productID INT UNSIGNED NOT NULL,
    userID INT UNSIGNED NOT NULL,
    rating DECIMAL(3,2),
    review TEXT,
    reviewDate DATE,
    reviewTitle VARCHAR(255),
    reviewAuthor VARCHAR(255),
    reviewAuthorRating DECIMAL(3,2),
    reviewAuthorLocation VARCHAR(255),
    reviewAuthorWebsite VARCHAR(255),    
    recommends BOOLEAN,
    helpfulCount INT DEFAULT 0,
    notHelpfulCount INT DEFAULT 0,
    reviewSource VARCHAR(255),
    verifiedPurchase BOOLEAN,
    responseToReview TEXT,
    responseDate DATE,
    responseAuthor VARCHAR(255),
    responseAuthorRating DECIMAL(3,2),
    responseAuthorLocation VARCHAR(255),
    responseAuthorWebsite VARCHAR(255),
    FOREIGN KEY (productID) REFERENCES Product(id),
    FOREIGN KEY (userID) REFERENCES User(id)
);

CREATE TABLE ProductOffers (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    productID INT UNSIGNED NOT NULL,
    offerType VARCHAR(255),
    offerDetails TEXT,
    offerStartDate DATE,
    offerEndDate DATE,
    discountPercentage DECIMAL(5,2),
    promoCode VARCHAR(255),
    minPurchaseQty INT,
    offerStatus VARCHAR(255),
    offerLimit INT,
    offerUsageCount INT,
    offerDescription TEXT,
    offerTerms VARCHAR(255),
    offerConditions TEXT,
    offerExclusions TEXT,
    offerHighlights TEXT,
    offerAdditionalInfo TEXT,
    offerLocation VARCHAR(255),
    offerAvailability VARCHAR(255),
    offerShippingInfo TEXT,
    offerSeller VARCHAR(255),
    offerSellerRating DECIMAL(3,2),
    offerSellerLocation VARCHAR(255),
    offerSellerContact VARCHAR(255),
    offerSellerWebsite VARCHAR(255),
    maxDiscountAmount DECIMAL(10,2),
    minPurchaseAmount DECIMAL(10,2),
    offerCategory VARCHAR(255),
    offerSubcategory VARCHAR(255),
    offerPrice DECIMAL(10,2),
    offerCreatedBy INT UNSIGNED,
    offerCreatedDate DATETIME,
    offerModifiedBy INT UNSIGNED,
    offerModifiedDate DATETIME,
    FOREIGN KEY (productID) REFERENCES Product(id)
);