CREATE TABLE Product (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255),
    brand VARCHAR(255),
    category VARCHAR(255),    
    slogan VARCHAR(255),
    description TEXT
);

CREATE TABLE ProductDetails (
    productID INT UNSIGNED NOT NULL,
    additionalProperty TEXT,
    aggregateRating JSON,
    audience VARCHAR(255),
    award VARCHAR(255),
    countryOfAssembly VARCHAR(255),
    countryOfLastProcessing VARCHAR(255),
    countryOfOrigin VARCHAR(255),
    funding VARCHAR(255),
    hasAdultConsideration BOOLEAN,
    hasEnergyConsumptionDetails BOOLEAN,
    hasMeasurement BOOLEAN,
    hasMerchantReturnPolicy BOOLEAN,
    inProductGroupWithID INT UNSIGNED,
    keywords VARCHAR(255),
    logo VARCHAR(255),
    manufacturer VARCHAR(255),
    model VARCHAR(255),
    mpn VARCHAR(255),
    negativeNotes VARCHAR(255),
    offers JSON,
    pattern VARCHAR(255),
    positiveNotes VARCHAR(255),
    productionDate DATE,
    purchaseDate DATE,
    releaseDate DATE,
    review JSON,
    FOREIGN KEY (productID) REFERENCES Product(id)
);
CREATE TABLE ProductPrice (
    productID INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    retailPrice DECIMAL(10,2),
    salePrice DECIMAL(10,2),
    discount DECIMAL(5,2),
    currency VARCHAR(10),
    availability VARCHAR(20),
    isAvailable BOOLEAN,
    isOnSale BOOLEAN,
    isFeatured BOOLEAN,
    isLimitedStock BOOLEAN,
    stockQuantity INT,
    soldQuantity INT,
    rating DECIMAL(3,2),
    reviewCount INT,
    createdAt TIMESTAMP,
    updatedAt TIMESTAMP,
    FOREIGN KEY (productID) REFERENCES Product(id)
)
CREATE TABLE ProductIdentifiers (
