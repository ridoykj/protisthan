    upc VARCHAR(32),
    color VARCHAR(255),
    depth DECIMAL(10,2),
    height DECIMAL(10,2),
    weight DECIMAL(10,2),
    width DECIMAL(10,2),
    material VARCHAR(255),
    unitType VARCHAR(255),
    quentatyOfEachUnit VARCHAR(255),    
    size VARCHAR(255),
    FOREIGN KEY (productID) REFERENCES Product(id)
);

